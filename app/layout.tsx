import type { Metadata } from "next";
import { Geist, Playfair_Display, Yellowtail } from "next/font/google";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
  display: "swap",
});

const playfair = Playfair_Display({
  variable: "--font-playfair",
  subsets: ["latin"],
  display: "swap",
});

const yellowtail = Yellowtail({
  variable: "--font-yellowtail",
  weight: "400",
  subsets: ["latin"],
  display: "swap",
});

export const metadata: Metadata = {
  title: "Marinali Rooms",
  description: "Generated by create next app",
};

import { SettingsService } from "@/lib/services/settingsService";
import Providers from "./providers";

import { Toaster } from "sonner";

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const settings = await SettingsService.getSettings();

  const primaryColor = settings.primaryColor;
  const secondaryColor = settings.secondaryColor;
  const backgroundColor = settings.backgroundColor;
  const textColor = settings.textColor;
  const fontFamily = settings.fontFamily;

  return (
    <html
      lang="en"
      className={`${geistSans.variable} ${playfair.variable} ${yellowtail.variable} antialiased`}
      style={{
        // @ts-ignore
        "--primary-color": primaryColor,
        "--secondary-color": secondaryColor,
        "--background": backgroundColor,
        "--background-color": backgroundColor,
        "--foreground": textColor,
        "--popover": backgroundColor,
        "--popover-foreground": textColor,
        "--font-primary": fontFamily,
      }}
    >
      <body className="min-h-screen flex flex-col">
        <Providers>
          {children}
          <Toaster position="top-center" richColors />
        </Providers>
      </body>
    </html>
  );
}
